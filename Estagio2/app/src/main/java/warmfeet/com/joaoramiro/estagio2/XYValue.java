public class XYValue {

    private  double x;
    private  double y;

    public  XYValue(double x, double y)
    {
        this.x=x;
        this.y=y;
    }

    public  double getX()
    {
        return x;
    }
    public  double getY()
    {
        return y;
    }

    public void  setX()
    {
        this.x=x;
    }

    public void  setY()
    {
        this.y=y;
    }
}
